script;
clear; clc; close all;
tau1 = 16; %задаём переменные 
tau2 = 5;
A = 1;
t = -10:1:100; %временной итервал 

l = 6; %параметры фильтра
k = 13;
m1 = 15;
m2 = 1;

y = Fexp(tau1, tau2, A, t);  %задаем функцию
m = Filter(l, k, m1, m2,y); % Дорогая Полина, молю, никогда, пожалуйста не используй в качестве названия функции имя встроенных функций 

plot(t,y/max(abs(y)),'g',t,m/max(abs(m)),'b');%строим график 
title('График')