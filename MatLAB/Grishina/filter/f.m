% [y] - функция, генерирующая массив чисел для построения графика функции  y=A*(exp(-t/t1)-exp(-t/t2));
% А - амплитуда функции;
% t1,t2 - входной параметр времени; 
% y - выходной параметр;
% t - выходной параметр времени;
% t0 = -10 (начальное значение), tmax = 100 (максимальное значение), шаг tstep = 1;
function y = f(t1, t2, A,t)

for i=length(t):-1:1
     if (t(i)<0)
            y(i) = 0;
     else 
            y(i) = A*(exp(-t(i)/t1) - exp(-t(i)/t2));
        end
    end

