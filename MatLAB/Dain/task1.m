function [y]=task1 (tau1,tau2,A,t)
% Функция генерирующая массив чисел 
% А - амплитута сигнала 
% tau1, tau2 - вход время
% y - выход, ось Y
% t - выход, ось X 
% t=(-10...100) времянной интервал 
% количество элементов массива 
k=length(t);
% вектор y(k)
y=1:k;
for n=1:k
if t(n)<0
y(n)=0;
else 
y(n)=A*(exp(-t(n)/tau1)-exp(-t(n)/tau2));  
end
end
end