%входные данные Exp1
t=-10:1:100; %вектор, интервал времени
A=1; %амплитуда сигнала
b1=16;
b2=5;
y = Exp1(t, A, b1, b2); %Вызов функции

%Параметры фильтра cups_like:
l = 6;
k = 13;
m1 = 16;
m2 = 1;

y1 = cups_like(l,k,m1,m2,y);
figure;%отобажение в двух окнах
plot(t, y);%построение графика 
grid on;%отображение сетки на графике
figure;
plot(t, y1);%построение фильтра
grid on;%отображение сетки на графике

