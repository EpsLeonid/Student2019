function [y]= v_exp (t1, t2, A, t)
%Файл-функция, генерирующая массив чисел и строящая график по формуле
%A-амплитуда сигнала
%t1, t2- входдные аргументы времени
%y-выходной аргумент, откладываемый по оси Y
%t-выходной аргумент, откладываемый по оси X
%t=(-10...150)- интервал времени
k=length(t); %количество элементов массива t
y=1:k; %вектор y(k)
for i=1:k
    if t(i)<0
        y(i)=0;
    else
        y(i)=A*(exp(-t(i)/t1)-exp(-t(i)/t2));  
    end
end
end