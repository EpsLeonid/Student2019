%Создание функции, согласно заданию №1, пункту 1

function [y]= first_one (t1, t2, t)

count = length(t); %Определяем длину массива t
y = 1:count; %Создаём массив y
A = 1; %Амплитуда

%Задание функции y
for x = 1 : count %Создаём цикл. Кол-во шагов равно длине массива t
    if t(x) <= 0
        y(x) = 0; %Определяем значения функции y для t меньшем, либо равном 0
    else
        y(x) = A*(exp(-(t(x)/t1)-exp(-t(x)/t2))); %Определяем значения функции y для t большем 0
    end
end